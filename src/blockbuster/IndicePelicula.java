public class IndicePelicula {
    int númeroPelícula;
    int índice;

    public IndicePelicula(int númeroPelícula, int índice) {
        this.númeroPelícula = númeroPelícula;
        this.índice = índice;
    }

    public int getNúmeroPelícula() {
        return númeroPelícula;
    }

    public void setNúmeroPelícula(int númeroPelícula) {
        this.númeroPelícula = númeroPelícula;
    }

    public int getÍndice() {
        return índice;
    }

    public void setÍndice(int índice) {
        this.índice = índice;
    }
    
    
}
